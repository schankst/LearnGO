package main

import "fmt"

func main() {

	fmt.Printf("true && true evaluates to %v \n", true && true)
	fmt.Printf("true && false evaluates to %v \n", true && false)
	fmt.Printf("true || true evaluates to %v \n", true || true)
	fmt.Printf("true || false evaluates to %v \n", true || false)
	fmt.Printf("!true evaluates to %v \n", !true)
}
